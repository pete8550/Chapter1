public class OneEight {

    public static void main(String[] args) {

        double radius = 6.5;
        double PI = 3.14159;

        double perimeter = 2 * radius * PI;
        double area = radius * radius * PI;

        System.out.println("The area of a circle is " + area + " and the perimeter is " + perimeter);
    }
}
