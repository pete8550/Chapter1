public class OneNine {

    public static void main(String[] args) {

        //The rectangle
        double width = 5.3;
        double height = 8.6;

        //The area and perimeter
        double area = width * height;
        double perimeter = 2 * (width + height);

        System.out.println("The area is " + area + " and the perimeter is " + perimeter);
    }
}
